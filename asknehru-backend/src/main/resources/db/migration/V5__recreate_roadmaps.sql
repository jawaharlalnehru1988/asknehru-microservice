DROP TABLE IF EXISTS roadmaps CASCADE;

CREATE TABLE roadmaps (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    main_topic VARCHAR(200) NOT NULL,
    intro TEXT,
    image_url VARCHAR(500),
    router_link VARCHAR(200),
    created_at TIMESTAMP(6) WITHOUT TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITHOUT TIME ZONE NOT NULL
);

CREATE TABLE roadmap_chapters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    roadmap_id BIGINT NOT NULL,
    chapter_name VARCHAR(500) NOT NULL,
    CONSTRAINT fk_roadmap FOREIGN KEY (roadmap_id) REFERENCES roadmaps(id) ON DELETE CASCADE
);

CREATE TABLE roadmap_subtopics (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    chapter_id BIGINT NOT NULL,
    subtopic_name VARCHAR(1000) NOT NULL,
    CONSTRAINT fk_chapter FOREIGN KEY (chapter_id) REFERENCES roadmap_chapters(id) ON DELETE CASCADE
);
